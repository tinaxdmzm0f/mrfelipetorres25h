package br.pucrio.dslmetrics.core.domain;

public class ProjectBuilderException extends Exception {

	private static final long serialVersionUID = 3999800915056779830L;

	public ProjectBuilderException() {
		super();
	}

	public ProjectBuilderException(String message, Throwable cause) {
		super(message, cause);
	}

	public ProjectBuilderException(String message) {
		super(message);
	}

	public ProjectBuilderException(Throwable cause) {
		super(cause);
	}
	
	

}
